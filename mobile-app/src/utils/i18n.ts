export type TKeys = keyof typeof translations.bn;
type LanguageCode = 'bn' | 'en';

// M10: dev-time key validation — warns in __DEV__ when a key is missing from translations
const _warnedKeys = new Set<string>();
function warnMissingKey(key: string, lang: LanguageCode): void {
  if (!__DEV__) return;
  const id = `${lang}:${key}`;
  if (_warnedKeys.has(id)) return;
  _warnedKeys.add(id);
  console.warn(`[i18n] Missing translation key "${key}" for lang="${lang}"`);
}

export const i18n = (language: LanguageCode = 'bn') => ({
  t: (key: TKeys): string => {
    const val = translations[language][key];
    if (val !== undefined) return val;
    const fallback = translations.bn[key];
    if (fallback !== undefined) {
      if (language !== 'bn') warnMissingKey(key, language);
      return fallback;
    }
    warnMissingKey(key, language);
    return `[${key}]`; // L10: bracket-wrap missing keys so they're visible in production
  },
});

