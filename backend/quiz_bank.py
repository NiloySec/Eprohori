def daily_indices(d: date | None = None, count: int = 5) -> list[int]:
    """Deterministic, rotating set of question indices for a given date.

    A window of `count` shifts by `count` each day, so consecutive days never
    overlap; over time it cycles through the whole bank.
    """
    d = d or date.today()
    n = len(QUESTIONS)
    start = (d.toordinal() * count) % n
    return [(start + i) % n for i in range(count)]


def daily_questions(d: date | None = None, count: int = 5) -> list[dict]:
    """Public question payload (NO answers) for today's quiz."""
    out = []
    for idx in daily_indices(d, count):
        q = QUESTIONS[idx]
        out.append({
            "id": idx,
            "q": q["q"],
            "options": [{"key": chr(97 + i), "text": t} for i, t in enumerate(q["options"])],
        })
    return out


def grade(answers: dict[str, str], d: date | None = None, count: int = 5) -> tuple[int, dict]:
    """Grade {id: key} against today's correct answers.

    Returns (score, correct_map) where correct_map = {id: correct_key}.
    """
    score = 0
    correct_map: dict[str, str] = {}
    for idx in daily_indices(d, count):
        correct_key = chr(97 + QUESTIONS[idx]["answer"])
        correct_map[str(idx)] = correct_key
        if answers.get(str(idx)) == correct_key:
            score += 1
    return score, correct_map
